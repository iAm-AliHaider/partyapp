"use client";

import { useState } from "react";
import { signIn } from "next-auth/react";
import Link from "next/link";
import Image from "next/image";
import { useRouter } from "next/navigation";
import { useLanguage, LanguageToggle } from "@/components/LanguageContext";
import { ArrowLeft, Phone, Lock, Eye, EyeOff, Loader2 } from "lucide-react";

export default function LoginPage() {
  const router = useRouter();
  const { t } = useLanguage();
  const [phone, setPhone] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const [loading, setLoading] = useState(false);
  const [showPassword, setShowPassword] = useState(false);
  const [rememberMe, setRememberMe] = useState(false);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError("");
    setLoading(true);
    const fullPhone = phone.startsWith("+92") ? phone : `+92${phone.replace(/^0/, "")}`;
    const result = await signIn("credentials", { phone: fullPhone, password, redirect: false });
    setLoading(false);
    if (result?.error) setError(t.auth.invalidCredentials);
    else router.push("/home");
  };

  return (
    <div className="min-h-screen bg-surface-secondary flex flex-col safe-area-inset">
      {/* Gradient accent strip */}
      <div className="h-1.5 bg-gradient-to-r from-accent via-accent/80 to-gold" />

      {/* Top nav */}
      <div className="flex justify-between items-center px-5 pt-4" style={{ paddingTop: "calc(env(safe-area-inset-top, 16px) + 8px)" }}>
        <Link href="/" className="w-9 h-9 rounded-full bg-surface-tertiary flex items-center justify-center tap-scale">
          <ArrowLeft size={18} className="text-label-secondary" />
        </Link>
        <LanguageToggle className="!bg-surface-tertiary !text-label-secondary !rounded-full !px-3 !py-1.5 text-subhead" />
      </div>

      <div className="flex-1 px-6 pt-8">
        {/* Hero illustration */}
        <div className="flex flex-col items-center mb-8">
          <div className="w-20 h-20 rounded-apple-xl bg-gradient-to-br from-accent to-accent-dark shadow-apple-lg flex items-center justify-center mb-4">
            <Image src="/icons/party-logo.png" alt="Logo" width={52} height={52} className="rounded-apple" />
          </div>
          <h1 className="text-title tracking-tight text-center">{t.auth.signInTitle}</h1>
          <p className="text-callout text-label-tertiary mt-1 text-center">{t.auth.welcome}</p>
        </div>

        <form onSubmit={handleSubmit} className="space-y-4">
          {error && (
            <div className="bg-red-50 text-red-700 px-4 py-3 rounded-apple-lg text-callout font-medium flex items-center gap-2">
              <div className="w-5 h-5 rounded-full bg-red-100 flex items-center justify-center flex-shrink-0">
                <span className="text-red-600 text-caption font-bold">!</span>
              </div>
              {error}
            </div>
          )}

          <div>
            <label className="text-subhead font-medium text-label-secondary mb-1.5 block">{t.auth.phoneNumber}</label>
            <div className="flex gap-2">
              <div className="input-field w-16 flex items-center justify-center text-callout font-medium text-label-secondary">+92</div>
              <div className="relative flex-1">
                <Phone size={16} className="absolute left-3.5 top-1/2 -translate-y-1/2 text-label-tertiary" />
                <input type="tel" value={phone} onChange={(e) => setPhone(e.target.value)} placeholder="3XX XXXXXXX" className="input-field !pl-10" required />
              </div>
            </div>
          </div>

          <div>
            <label className="text-subhead font-medium text-label-secondary mb-1.5 block">{t.auth.password}</label>
            <div className="relative">
              <Lock size={16} className="absolute left-3.5 top-1/2 -translate-y-1/2 text-label-tertiary" />
              <input
                type={showPassword ? "text" : "password"}
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                placeholder={t.auth.enterPassword}
                className="input-field !pl-10 !pr-12"
                required
              />
              <button
                type="button"
                onClick={() => setShowPassword(!showPassword)}
                className="absolute right-3.5 top-1/2 -translate-y-1/2 text-label-tertiary tap-scale"
              >
                {showPassword ? <EyeOff size={16} /> : <Eye size={16} />}
              </button>
            </div>
          </div>

          {/* Remember me + Forgot */}
          <div className="flex items-center justify-between">
            <label className="flex items-center gap-2 cursor-pointer">
              <div
                onClick={() => setRememberMe(!rememberMe)}
                className={`w-5 h-5 rounded-md flex items-center justify-center transition-all ${
                  rememberMe ? "bg-accent" : "bg-surface-tertiary border border-label-quaternary"
                }`}
              >
                {rememberMe && (
                  <svg width="12" height="12" viewBox="0 0 12 12" fill="none">
                    <path d="M2.5 6L5 8.5L9.5 3.5" stroke="white" strokeWidth="1.5" strokeLinecap="round" strokeLinejoin="round" />
                  </svg>
                )}
              </div>
              <span className="text-subhead text-label-secondary">{(t.auth as any).rememberMe || "Remember me"}</span>
            </label>
            <button type="button" className="text-subhead text-accent font-medium">
              {(t.auth as any).forgotPassword || "Forgot password?"}
            </button>
          </div>

          <button type="submit" disabled={loading} className="btn-primary w-full mt-2 flex items-center justify-center gap-2">
            {loading && <Loader2 size={18} className="animate-spin" />}
            {loading ? t.auth.signingIn : t.auth.signInTitle}
          </button>
        </form>

        {/* Divider */}
        <div className="flex items-center gap-3 my-6">
          <div className="flex-1 h-px bg-separator" />
          <span className="text-caption text-label-quaternary">{(t.auth as any).or || "or"}</span>
          <div className="flex-1 h-px bg-separator" />
        </div>

        {/* Social login placeholder */}
        <button
          type="button"
          disabled
          className="w-full flex items-center justify-center gap-3 py-3.5 rounded-apple-lg bg-surface-primary shadow-apple text-body font-medium text-label-secondary opacity-50"
        >
          <svg width="18" height="18" viewBox="0 0 24 24">
            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4" />
            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
          </svg>
          {(t.auth as any).continueWithGoogle || "Continue with Google"}
        </button>

        <div className="mt-8 text-center pb-8">
          <p className="text-callout text-label-tertiary">
            {t.auth.noAccount}{" "}
            <Link href="/register" className="text-accent font-semibold">{t.auth.register}</Link>
          </p>
        </div>
      </div>

      <div className="safe-area-bottom" />
    </div>
  );
}
